package Notcoder;

public class Kadai0103 {
    public static void main(String[] args) {
        String animal = "インドゾウ";
        String name = "エレガント";
        char gender = '男';
        int high = 320;
        long weight = 2000000;
        String area = "インド";
        //使用した変数: int long char String

        System.out.println("シスエボ動物園にいる動物のデータ");
        System.out.println("種類 :" + animal);
        System.out.println("名前 :" + name);
        System.out.println("性別 :" + gender);
        System.out.println("身長 :" + high);
        System.out.println("体重 :" + weight);
        System.out.println("出身地" + area);

        System.out.println();//空白行の生成

        //ぱおんちゃんのデータ
        animal = "アフリカゾウ";
        name = "ぱおん";
        gender = '女';
        high = 330;
        weight = 1800000;
        area = "アフリカ";
        System.out.println("種類 :" + animal);
        System.out.println("名前 :" + name);
        System.out.println("性別 :" + gender);
        System.out.println("身長 :" + high);
        System.out.println("体重 :" + weight);
        System.out.println("出身地" + area);
    }
}
